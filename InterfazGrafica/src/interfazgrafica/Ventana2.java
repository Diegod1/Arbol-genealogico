    private void consultar(String consulta)
    {
           Query con=new Query(consulta);
           //LinkedList<String> d=new LinkedList<>();
           
           
           if(con.hasSolution())
           {    
               
               for (Map<String, Term> map : con) {
                   String datos=map.get("X").toString();
                   //d.add(datos);
                   //arreglo.add(datos);
                   texto.setText(texto.getText()+"\n"+datos);
                  
               }
               
               
               
           }
           
          
           
    }
